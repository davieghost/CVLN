$('#showit-01').click(function(){
	$('#bigshow-01').animate({
		opacity: 1
	});
	$('#bigshow-01').css('display', 'inline-block');
	$('#personality-01').css('display', 'none');
	$('.flip').css('opacity','0');
});
$('#bigshow-01').click(function(e){
	if(e.target === this){
	$('#bigshow-01').animate({
		opacity: 0
	});
	$('#bigshow-01').css('display', 'none');
	$('.flip').css('opacity','1');};
	
});
